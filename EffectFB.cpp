#include "EffectFB.h"

#include "enemyRE.h"
#include "EffectFeelBetter.h"

#include "S-Editor.h"

//*****************************************************************************
// グローバル変数
//*****************************************************************************
EffectFeelBetter EffectFB[ENEMY_MAX];

//=============================================================================
// 初期化処理
//=============================================================================
void InitEffectFB(void)
{
	for (int i = 0; i < ENEMY_MAX; i++)
	{
		EffectFB[i].Init();
	}
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitEffectFB(void)
{
	for (int i = 0; i < ENEMY_MAX; i++)
	{
		EffectFB[i].Uninit();
	}
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateEffectFB(void)
{
	for (int i = 0; i < ENEMY_MAX; i++)
	{
		EffectFB[i].Update();
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawEffectFB(void)
{
	for (int i = 0; i < ENEMY_MAX; i++)
	{
		EffectFB[i].Draw();
	}
}

//=============================================================================
// エフェクト発生
//=============================================================================
void CallEffectFB(Vector3 pos)
{
	for (int i = 0; i < ENEMY_MAX; i++)
	{
		if (!EffectFB[i].CheckState())
		{
			EffectFB[i].Call(pos, EFX_SIZE_DEFEAT);
			break;
		}
	}
}
